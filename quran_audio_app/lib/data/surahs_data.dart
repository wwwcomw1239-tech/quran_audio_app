/// Get a Surah by its ID (1-114)
SurahModel? getSurahById(int id) {
  try {
    return allSurahs.firstWhere((surah) => surah.id == id);
  } catch (e) {
    return null;
  }
}

/// Get all Meccan Surahs
List<SurahModel> get makkiSurahs =>
    allSurahs.where((surah) => surah.type == 'مكية').toList();

/// Get all Medinan Surahs
List<SurahModel> get madaniSurahs =>
    allSurahs.where((surah) => surah.type == 'مدنية').toList();

/// Total number of verses in the Quran
int get totalVersesCount =>
    allSurahs.fold(0, (sum, surah) => sum + surah.versesCount);
